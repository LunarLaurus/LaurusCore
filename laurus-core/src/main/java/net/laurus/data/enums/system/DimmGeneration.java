package net.laurus.data.enums.system;

/**
 * Enum representing different generations of DIMM (Dual Inline Memory Module) memory.
 */
public enum DimmGeneration {

    /** First-generation DDR memory */
    DDR,

    /** Second-generation DDR2 memory */
    DDR2,

    /** Third-generation DDR3 memory */
    DDR3,

    /** Fourth-generation DDR4 memory */
    DDR4,

    /** Fully Buffered DIMM DDR2 memory */
    FBD2,

    /** Low-power DDR3 memory */
    LPDD3,

    /** Low-power DDR memory */
    LPDDR,

    /** Low-power DDR2 memory */
    LPDDR2,

    /** Low-power DDR4 memory */
    LPDDR4;
}
